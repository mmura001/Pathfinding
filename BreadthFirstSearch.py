def is_valid(x, y, maze):
    return 0 <= x < len(maze) and 0 <= y < len(maze[0]) and maze[x][y] != 1

def find_shortest_path(maze, start, end):
    queue = deque([(start, [])]) 
    visited = set()
    
    while queue:
        (x, y), path = queue.popleft()
        if (x, y) == end:
            return path + [(x, y)]
        
        for dx, dy in [(0, 1), (0, -1), (1, 0), (-1, 0)]:
            new_x, new_y = x + dx, y + dy
            if is_valid(new_x, new_y, maze) and (new_x, new_y) not in visited:
                queue.append(((new_x, new_y), path + [(x, y)]))
                visited.add((new_x, new_y))
                
    return []

def find_path_min_ones(maze, start, end):
    # Try to find a path with only 0s
    path = find_shortest_path(maze, start, end)
    if path:
        return path
    
    # No 0-only path, so find path minimizing 1s
    queue = deque([(start, 0, [])]) 
    visited = set()
    
    while queue:
        (x, y), ones, path = queue.popleft()
        
        if (x, y) == end:
            return path + [(x, y)]
        
        for dx, dy in [(0, 1), (0, -1), (1, 0), (-1, 0)]:
            new_x, new_y = x + dx, y + dy
            
            if is_valid(new_x, new_y, maze) and (new_x, new_y) not in visited:
                new_ones = ones + maze[new_x][new_y]
                queue.append(((new_x, new_y), new_ones, path + [(x, y)]))
                visited.add((new_x, new_y))
                
    return []


path = find_path_min_ones(maze, start, end)
